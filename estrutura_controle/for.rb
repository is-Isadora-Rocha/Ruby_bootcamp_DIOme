# for = durante - usado para percorrer uma coleção de elementos
#leia uma lista (array) de elementos - linguagens de programação

#lista
linguagens = ['Ruby', 'Go', 'C++', 'Java']
count = 1

#printar a lista na tela (puts)
#laço for
for linguagem in linguagens
    puts linguagem
    puts count
    count = count + 1
end